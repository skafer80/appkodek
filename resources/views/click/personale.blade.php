            function refreshStatusAutocriteri() {
                $.ajax({
                    url: 'valutazione/autocriteri/status'
                    , type: 'GET'
                    , success: function(data, textstatus, jqxhr) {
                        $('#progress .progress-bar').attr('aria-valuenow', data);
                        $('#progress .progress-bar').css('width', data + '%');
                        $('#progress .progress-bar').html(data + '%');
                        if (data == 100) {
                            clearInterval(timer);
                            setTimeout(resetStatusAutocriteri, 5000);
                        }
                    }
                });
